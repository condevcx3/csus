				<h1>Introduction</h1>
				<p>The Computer Science Undergraduate Society is the organization that represents all computer science students at <a href="http://uwo.ca">Western University</a> in London, Ontario. As the oldest and most established computer science organization on campus, CSUS is run by students and for students. We work closely with the <a href="http://www.csd.uwo.ca/">Department of Computer Science</a> to promote computer science as an academic discipline, to connect students to related university services, and to provide methods of networking between students, faculty, and employers.</p>
			</section>
			<section id="events">
				<h1>Events</h1>
				<p>Throughout the year, CSUS runs a number of events for students, bridging the gap between the academic world and social life. In past years, we have organized events both on and off campus, including:</p>
				<ul>
					<li>Laser Tag</li>
					<li>Bowling Night</li>
					<li>Pub Nights</li>
					<li>Exam prep and review</li>
				</ul>
				<p>By far, our most popular event is our annual Academic Colloquium, where students gather for an evening of talks presented by the department's leading researchers. Be sure to keep in touch with us to find out what we'll be doing next!</p>
